
public class operator02 {
    public static void main(String[] args){
        //cara pemakaian int
        int bil1, bil2, hasil;

        bil1 = 6;
        bil2 = 5;
        hasil = bil1 + bil2;
        
        System.out.println(bil1 + " + " + bil2 + " = " + hasil);

        //cara ribet
        System.out.print("5 + 6 = ");
        System.out.print(5 + 6);

        //tuliskan 5 + 6 = 11
        System.out.println();
        //cara pemakaian double
        double bil3, bil4, hasil2;

        bil3 = 4;
        bil4 = 2;
        hasil2 = bil3 / bil4;
        
        System.out.println(bil3 + " / " + bil4 + " = " + hasil2);




    }
}
