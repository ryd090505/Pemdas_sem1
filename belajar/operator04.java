

public class operator04 {
    public static void main(String[] args){
        int bil1 = 13;

        bil1--; // bil1 = 12
        bil1--; // bil2 = 11

        System.out.println(bil1);

        int bil2 = 13;

        --bil2; 
        // bil1--; 

        System.out.println(bil2);

        //cara menggunakan -

        int bil3 = 13;

        bil3++; //bil1 = 14
        bil3++; //bil2 = 15

        System.out.println(bil3);

        //cara menggunakan +

    

    }

}
